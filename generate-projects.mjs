// Turn the curation workflow's output (curation-result.json) + tg-groups.json
// into src/projects.js for the gallery.
import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';

const WORK = path.resolve('./public/work');

// ffprobe real pixel dimensions (works for jpg and mp4), cached per file
const dimCache = {};
function dims(file) {
  if (dimCache[file]) return dimCache[file];
  try {
    const out = execSync(
      `ffprobe -v error -select_streams v:0 -show_entries stream=width,height -of csv=p=0 "${path.join(WORK, file)}"`,
      { encoding: 'utf8' }
    ).trim().split(/[,\r\n]+/);
    const w = +out[0], h = +out[1];
    dimCache[file] = w && h ? { w, h } : null;
  } catch { dimCache[file] = null; }
  return dimCache[file];
}
const groups = JSON.parse(fs.readFileSync('./tg-groups.json', 'utf8'));
const result = JSON.parse(fs.readFileSync('./curation-result.json', 'utf8'));
const byPost = Object.fromEntries(groups.map((g) => [g.post, g]));
const curatedByPost = Object.fromEntries((result.curated || []).map((c) => [c.post, c]));

const CAT_COLOR = {
  POSTER: '#ff5a1f', LOGO: '#c9ff3d', SOCIAL: '#8a6bff', MOTION: '#57b0ff',
  '3D': '#ff3d6e', VIDEO: '#ffd23e', BRAND: '#5ce8c4', PACKAGE: '#ffa3d1',
};

const slug = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
const exists = (f) => fs.existsSync(path.join(WORK, f));
const mb = (f) => fs.statSync(path.join(WORK, f)).size / 1048576;

const projects = [];
const seenIds = new Set();

for (const p of result.final) {
  const id0 = slug(p.title) || `p${p.posts[0]}`;
  let id = id0, n = 2;
  while (seenIds.has(id)) id = `${id0}-${n++}`;
  seenIds.add(id);

  // build media: per member post — curator-kept photo stills first, then videos
  const media = [];
  const seenSrc = new Set();
  for (const postId of p.posts) {
    const g = byPost[postId];
    if (!g) continue;
    const cur = curatedByPost[postId];
    const keep = cur?.gallery?.length ? cur.gallery : [...g.photos, ...g.frames];
    for (const f of keep) {
      // photos only in the stack (frames serve as video posters, thumbs are junk)
      if (!/_photo\d+\.jpg$/.test(f) || !exists(f) || seenSrc.has(f)) continue;
      seenSrc.add(f);
      const d = dims(f);
      media.push({ type: 'image', src: `/work/${f}`, ...(d || {}) });
    }
    g.videos.forEach((v, i) => {
      if (!exists(v) || seenSrc.has(v)) return;
      seenSrc.add(v);
      const poster = `post${postId}_frame${i}.jpg`;
      const d = dims(v);
      media.push({ type: 'video', src: `/work/${v}`, poster: exists(poster) ? `/work/${poster}` : null, ...(d || {}) });
    });
  }
  // a project must show something
  if (!media.length && p.cover && exists(p.cover)) media.push({ type: 'image', src: `/work/${p.cover}` });
  if (!media.length) { console.log('SKIP (no media):', p.title); continue; }

  const cover = p.cover && exists(p.cover)
    ? `/work/${p.cover}`
    : media.find((m) => m.type === 'image')?.src || media[0].poster;
  if (!cover) { console.log('SKIP (no cover):', p.title); continue; }

  // hover preview — strict perceptual continuity with the card image:
  // 1) if the cover IS a video frame (postN_frameI.jpg), play THAT video;
  // 2) else only a video from the same post as the cover, orientation-matched;
  // 3) otherwise no preview (a mismatched video reads as a wrong thumbnail).
  const coverFile = cover.replace('/work/', '');
  const coverDims = dims(coverFile);
  const coverPortrait = coverDims ? coverDims.h > coverDims.w : false;
  let preview = null;

  const frameMatch = coverFile.match(/^post(\d+)_frame(\d+)\.jpg$/);
  if (frameMatch) {
    const v = `post${frameMatch[1]}_video${frameMatch[2]}.mp4`;
    if (exists(v) && mb(v) <= 9) preview = `/work/${v}`;
  }
  if (!preview) {
    const coverPost = +(coverFile.match(/^post(\d+)_/) || [])[1];
    const sameKind = (byPost[coverPost]?.videos || [])
      .filter((v) => exists(v) && mb(v) <= 9)
      .map((v) => ({ v, d: dims(v) }))
      .filter(({ d }) => d && (d.h > d.w) === coverPortrait);
    if (sameKind.length) preview = `/work/${sameKind[0].v}`;
  }

  projects.push({
    id,
    title: p.title,
    client: p.client,
    year: p.year,
    cat: p.cat,
    tags: (p.tags || []).slice(0, 3).map((t) => t.toUpperCase()),
    cover,
    preview,
    media: media.slice(0, 12),
    blurb: p.blurb,
    role: p.role,
    tools: p.tools,
  });
}

const cats = ['ALL', ...new Set(projects.map((p) => p.cat))];

const out = `/* AUTO-GENERATED by generate-projects.mjs — curated from t.me/donishyarportfolio (${new Date().toISOString().slice(0, 10)}). */
export const CAT_COLOR = ${JSON.stringify(CAT_COLOR, null, 2)};

export const CATEGORIES = ${JSON.stringify(cats)};

export const PROJECTS = ${JSON.stringify(projects, null, 2)};
`;
fs.writeFileSync('./src/projects.js', out);
console.log(`projects: ${projects.length}`);
console.log('categories:', cats.join(', '));
console.log('with preview video:', projects.filter((p) => p.preview).length);
console.log('total media items:', projects.reduce((s, p) => s + p.media.length, 0));
